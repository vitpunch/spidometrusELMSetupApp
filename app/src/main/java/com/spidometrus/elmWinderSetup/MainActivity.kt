        suspend fun sendDataForNewVersion(data : String) = coroutineScope {
            launch {
                while (!deny){
                    Log.d("deny", "False")
                    delay(20)
                }
                deny=false
                Log.d("deny","True")
                serialPort.sendData(data)
            }
        }

        suspend fun sendDataForOldVersion(data : String) = coroutineScope {
            launch {
                delay(200)

                serialPort.sendData(data)
            }
        }


